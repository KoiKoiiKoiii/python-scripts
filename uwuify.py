# String to UwUify:
uwuString = "This is fun."

# UwUify process:
uwuString = uwuString.replace("r", "w").replace("l", "w").replace("R", "W").replace("L", "W")
uwuString = uwuString.replace("you", "u").replace("You", "u")
uwuString = uwuString.replace("the", "da").replace("The", "Da")
uwuString = uwuString.replace("this", "dis").replace("This", "Dis")
uwuString = uwuString.replace("s", "sh").replace("S", "Sh")
uwuString = uwuString.replace("ove", "uv").replace("Ove", "Uve")
uwuString = uwuString.replace("!", " owo!").replace("?", " owo?")

# Print UwUified string:
print(uwuString)